package Tushar.com;

import java.util.Scanner;

public class REversedigit {

	public static void main(String[] args) {
		// reverse the with for loop

		int   n ;
		System.out.println("Enter the number ");
		Scanner sc = new Scanner(System.in);
		n= sc.nextInt();
		for(int i=n ;i>= 1 ;i--)
		{
			System.out.println(" "+i+" ");
		}
	}

}
